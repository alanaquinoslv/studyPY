# conjunto de funções matemáticas integradas

import math

Tupla = (10, 20, 30, 40)

print(min(Tupla))  # valor minimo
print(max(Tupla))  # valor maximo

# transformar num negativo em positivo
print(abs(-5.43))

# potencia
print(pow(3, 4))

# raiz quadrada
print(math.sqrt(81))

# arredondamento
print(math.ceil(1.4))  # ceil arredonda para cima ate o inteiro mais proximo
print(math.floor(1.4))  # floor arredonda para baixo ate o inteiro mais proximo

# pi
print(math.pi)
